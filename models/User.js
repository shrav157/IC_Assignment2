const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
        trim: true
    },
    password: {
        type: String,
        required: true
    },
        isSubscribed: {
            type: Boolean,
            default: false // Default to non-subscribed
        }
    });

module.exports = mongoose.model('User', userSchema);
